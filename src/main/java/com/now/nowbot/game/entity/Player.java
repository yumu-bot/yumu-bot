package com.now.nowbot.game.entity;

public class Player {
    float hp;
    float act;
    float speed;

    public float getHp() {
        return hp;
    }

    public void setHp(float hp) {
        this.hp = hp;
    }

    public float getAct() {
        return act;
    }

    public void setAct(float act) {
        this.act = act;
    }

    public float getSpeed() {
        return speed;
    }

    public void setSpeed(float speed) {
        this.speed = speed;
    }
}
