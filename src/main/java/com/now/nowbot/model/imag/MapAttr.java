package com.now.nowbot.model.imag;
public class MapAttr {
    Long id;
    Long bid;
    Integer mods;

    Float ar;
    Float od;
    Float cs;
    Float hp;

    Float arWindow;
    Float odWindow;

    Float stars;
    Float bpm;
    Integer combo;

    public Long getId() {
        return id;
    }

    public Long getBid() {
        return bid;
    }

    public void setBid(Long bid) {
        this.bid = bid;
    }

    public Integer getMods() {
        return mods;
    }

    public void setMods(Integer mods) {
        this.mods = mods;
    }

    public Float getAr() {
        return ar;
    }

    public void setAr(Float ar) {
        this.ar = ar;
    }

    public Float getOd() {
        return od;
    }

    public void setOd(Float od) {
        this.od = od;
    }

    public Float getCs() {
        return cs;
    }

    public void setCs(Float cs) {
        this.cs = cs;
    }

    public Float getHp() {
        return hp;
    }

    public void setHp(Float hp) {
        this.hp = hp;
    }

    public Float getArWindow() {
        return arWindow;
    }

    public void setArWindow(Float arWindow) {
        this.arWindow = arWindow;
    }

    public Float getOdWindow() {
        return odWindow;
    }

    public void setOdWindow(Float odWindow) {
        this.odWindow = odWindow;
    }

    public Float getStars() {
        return stars;
    }

    public void setStars(Float stars) {
        this.stars = stars;
    }

    public Float getBpm() {
        return bpm;
    }

    public void setBpm(Float bpm) {
        this.bpm = bpm;
    }

    public Integer getCombo() {
        return combo;
    }

    public void setCombo(Integer combo) {
        this.combo = combo;
    }
}
